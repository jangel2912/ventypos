    function validarCompletado(){
        
        var puntuacion = 0;
        
        puntuacion = $('#completadoNit').val() == "" ? ( puntuacion + 0 ) : ( puntuacion + 1 );
        puntuacion = $('#completadoNombre').val() == "" ? ( puntuacion + 0 ) : ( puntuacion + 1 );
        puntuacion = $('#inputLogo').val() == "" ? ( puntuacion + 0 ) : ( puntuacion + 1 );
        
        if( puntuacion == 0) animarCompletado(25)
        if( puntuacion == 1) animarCompletado(50)
        if( puntuacion == 2) animarCompletado(75)
        if( puntuacion == 3) animarCompletado(97)
    }        
                
    function animarCompletado(number){
        
        var actual = $('#completadoTxt').text();
        
        if(number==25) $("#completadoBar").css("cssText", "width: "+number+"%; background-color: #DE6E6E !important;");        
        if(number==50) $("#completadoBar").css("cssText", "width: "+number+"%; background-color: #DEBF6E !important;");
        if(number==75) $("#completadoBar").css("cssText", "width: "+number+"%; background-color: #B4DE6E !important;");
        if(number==97) $("#completadoBar").css("cssText", "width: "+number+"%; background-color: #6E8DDE !important;");

        if(number==25) $("#contCompletadoTxt").css("cssText", " color: #DE6E6E !important;");        
        if(number==50) $("#contCompletadoTxt").css("cssText", " color: #DEBF6E !important;");
        if(number==75) $("#contCompletadoTxt").css("cssText", " color: #B4DE6E !important;");
        if(number==97) $("#contCompletadoTxt").css("cssText", " color: #6E8DDE !important;");        
        
        
        setTimeout(function(){           

            $('#completadoTxt').stop().prop('number', actual ).animateNumber(
              {
                number: number,
                easing: 'easeInOutExpo', // require jquery.easing

                // optional custom step function
                // using here to keep '%' sign after number
                numberStep: function(now, tween) {
                  var floored_number = Math.floor(now);
                  var target = $(tween.elem);
                  target.text(floored_number);
                }
              },
              1000
            );  
    
        },0);
    }

